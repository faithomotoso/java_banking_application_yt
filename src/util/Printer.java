package util;

public final class Printer {
    private Printer(){};

    /**
     * Prints a horizontal line made of '='
     */
    public static void printEqualsLine() {
        System.out.println("===============================================");
    }

    /**
     * Prints a horizontal line made of '-'
     */
    public static void printDashLine() {
        System.out.println("-----------------------------------------------");
    }
}
